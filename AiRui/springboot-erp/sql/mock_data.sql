SELECT s.supplier_code,
       s.supplier_name,
       COUNT(po.id)         as 订单数,
       SUM(po.final_amount) as 采购金额
FROM supplier s
         LEFT JOIN purchase_order po ON s.id = po.supplier_id
WHERE s.status = 1
GROUP BY s.id
ORDER BY 采购金额 DESC
LIMIT 5;

-- 5. 查看客户销售统计
SELECT c.customer_code,
       c.customer_name,
       COUNT(so.id)         as 订单数,
       SUM(so.final_amount) as 销售金额
FROM customer c
         LEFT JOIN sales_order so ON c.id = so.customer_id
WHERE c.status = 1
GROUP BY c.id
ORDER BY 销售金额 DESC
LIMIT 5;

